package final03;

public class P01Country {
		/* 상수를 사용하는 방법
		 * 상수는 값변경이 불가능하기 때문에 public
		 * static이 붙은 변수는 공용으로 사용되며 객체 생성없이도 호출이 가능
		 * final이 붙은 변수는 값 수정이 불가능
		 */
		public static final int KOREA = 1003;
		public static final int CHINA = 96000;
		public static final int STATES = 98315;
		public static final int CANADA = 99846;
		
}
