package math01;

public class R01 {
	public static void main(String[] args) {
		int a = -3;
		System.out.println(Math.abs(a));
		
		double c = -3.14;
		System.out.println(Math.abs(c));
		System.out.println((int)Math.abs(c));
		System.out.println(Math.ceil(c));
		System.out.println(Math.floor(c));
	}
}
