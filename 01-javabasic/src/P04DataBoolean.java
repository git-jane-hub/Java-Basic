
public class P04DataBoolean {
	public static void main(String[] args) {
		/*
		 *  boolean 자료형은 참/거짓을 구분할 때 사용
		 *  이 자료형의 변수에는 오직 true/false만 담을 수 있음
		 */
		boolean a = true;
		boolean b = false;
		// 다른 언어와는 달리 숫자로 표시할 수 없음
//		boolean c = 0;
		/*  다른 에러의 선택지와는 달리 에러를 수정하는 부분의 선택지가 1개(우변에 작성할 변수값만 변경 가능)
		 *  다른 에러: 1. 좌변의 데이터타입을 변경 2. 우변의 변수값을 변경
		 */
		System.out.println(a);
		System.out.println(b);
	}
}
